package aula_07.exercicios;

// Crie métodos para realizar operações básicas (soma,  
// subtração, multiplicação e divisão) entre dois números;
public class Exercicio1 {
    public static double soma(int a, int b) {
        return a + b;
    }

    public static double subtracao(int a, int b) {
        return a - b;
    }

    public static double multiplicar(int a, int b) {
        return a * b;
    }

    public static double dividir(int a, int b) {
        return a / b;
    }
}
